#include "3-calc.h"
/**
 * op_add - addition
 * @a: argument1 to operate
 * @b: argument2 to operate
 * Return: resultado
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - addition
 * @a: argument1 to operate
 * @b: argument2 to operate
 * Return: resultado
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - addition
 * @a: argument1 to operate
 * @b: argument2 to operate
 * Return: resultado
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - addition
 * @a: argument1 to operate
 * @b: argument2 to operate
 * Return: add
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: argument1 to operate
 * @b: argument2 to operate
 * Return: resultado
 */
int op_mod(int a, int b)
{
	return (a % b);
}
